package com.example.idscomercial.examen1.ui.dapterutils;


public class DataRow {
    private String idRow;
    private String nombreRow;
    private String apellidosRow;
    private String direccionRow;
    private String telefonoRow;
    private String mailRow;
    private String fechaNacimientoRow;
    private String edoCivilRow;
    private String usuarioRow;
    private String contraseñaRow;

    public DataRow() {
    }

    public String getIdRow() {
        return idRow;
    }

    public void setIdRow(String idRow) {
        this.idRow = idRow;
    }

    public String getNombreRow() {
        return nombreRow;
    }

    public void setNombreRow(String nombreRow) {
        this.nombreRow = nombreRow;
    }

    public String getApellidosRow() {
        return apellidosRow;
    }

    public void setApellidosRow(String apellidosRow) {
        this.apellidosRow = apellidosRow;
    }

    public String getDireccionRow() {
        return direccionRow;
    }

    public void setDireccionRow(String direccionRow) {
        this.direccionRow = direccionRow;
    }

    public String getTelefonoRow() {
        return telefonoRow;
    }

    public void setTelefonoRow(String telefonoRow) {
        this.telefonoRow = telefonoRow;
    }

    public String getMailRow() {
        return mailRow;
    }

    public void setMailRow(String mailRow) {
        this.mailRow = mailRow;
    }

    public String getFechaNacimientoRow() {
        return fechaNacimientoRow;
    }

    public void setFechaNacimientoRow(String fechaNacimientoRow) {
        this.fechaNacimientoRow = fechaNacimientoRow;
    }

    public String getEdoCivilRow() {
        return edoCivilRow;
    }

    public void setEdoCivilRow(String edoCivilRow) {
        this.edoCivilRow = edoCivilRow;
    }

    public String getUsuarioRow() {
        return usuarioRow;
    }

    public void setUsuarioRow(String usuarioRow) {
        this.usuarioRow = usuarioRow;
    }

    public String getContraseñaRow() {
        return contraseñaRow;
    }

    public void setContraseñaRow(String contraseñaRow) {
        this.contraseñaRow = contraseñaRow;
    }
}
